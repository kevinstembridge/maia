package org.maiaframework.gen.renderers

import org.maiaframework.gen.spec.definition.TimelineBlotterDef
import org.maiaframework.gen.spec.definition.lang.DomainIdFieldType
import org.maiaframework.gen.spec.definition.lang.EnumFieldType
import org.maiaframework.gen.spec.definition.lang.InstantFieldType
import org.maiaframework.gen.spec.definition.lang.IntFieldType
import org.maiaframework.gen.spec.definition.lang.IntTypeFieldType
import org.maiaframework.gen.spec.definition.lang.IntValueClassFieldType
import org.maiaframework.gen.spec.definition.lang.LongFieldType
import org.maiaframework.gen.spec.definition.lang.LongTypeFieldType
import org.maiaframework.gen.spec.definition.lang.StringFieldType
import org.maiaframework.gen.spec.definition.lang.StringTypeFieldType
import org.maiaframework.gen.spec.definition.lang.StringValueClassFieldType


class TimelineBlotterRowDtoDomainRenderer(
    private val def: TimelineBlotterDef
) : AbstractSourceFileRenderer() {


    override fun renderedFilePath(): String {
        return def.rowDtoFqcn.toString().replace(".", "/") + ".kt"
    }


    override fun renderSource(): String {

        val imports = mutableSetOf(
            "java.time.Instant",
            "org.maiaframework.domain.ChangeType",
            "org.maiaframework.domain.DomainId"
        )

        def.entityHistoryColumns.forEach { col ->
            when (val ft = col.classFieldDef.fieldType) {
                is EnumFieldType -> imports.add(ft.fqcn.toString())
                is IntTypeFieldType -> imports.add(ft.fqcn.toString())
                is IntValueClassFieldType -> imports.add(ft.fqcn.toString())
                is LongTypeFieldType -> imports.add(ft.fqcn.toString())
                is StringTypeFieldType -> imports.add(ft.fqcn.toString())
                is StringValueClassFieldType -> imports.add(ft.fqcn.toString())
                else -> {}
            }
        }

        appendLine("// This source was generated by the Maia Framework code generator")
        appendLine("// Renderer class: ${this::class.qualifiedName}")
        blankLine()
        appendLine("package ${def.packageName}")
        blankLine()
        imports.sorted().forEach { appendLine("import $it") }
        blankLine()
        blankLine()
        appendLine("data class ${def.rowDtoUqcn}(")
        appendLine("    val eventTimestamp: Instant,")
        appendLine("    val eventType: String,")
        appendLine("    val changeType: ChangeType?,")
        appendLine("    val version: Long?,")

        def.entityHistoryColumns.forEach { col ->
            val fieldName = col.classFieldDef.classFieldName.value
            val typeName = toKotlinTypeName(col.classFieldDef.fieldType)
            appendLine("    val $fieldName: $typeName?,")
        }

        def.joinDefs.forEach { joinDef ->
            appendLine("    val ${joinDef.rightFkDtoFieldName}: DomainId?,")
            appendLine("    val ${joinDef.displayFieldDtoFieldName}: String?,")
        }

        appendLine(") {")
        blankLine()
        blankLine()
        appendLine("}")
        blankLine()

        return sourceCode.toString()

    }


    private fun toKotlinTypeName(fieldType: org.maiaframework.gen.spec.definition.lang.FieldType): String {
        return when (fieldType) {
            is InstantFieldType -> "Instant"
            is IntFieldType -> "Int"
            is IntTypeFieldType -> fieldType.uqcn.value
            is IntValueClassFieldType -> fieldType.uqcn.value
            is LongFieldType -> "Long"
            is LongTypeFieldType -> fieldType.uqcn.value
            is StringFieldType -> "String"
            is StringTypeFieldType -> fieldType.uqcn.value
            is StringValueClassFieldType -> fieldType.uqcn.value
            is DomainIdFieldType -> "DomainId"
            is EnumFieldType -> fieldType.fqcn.uqcn.value
            else -> "String"
        }
    }


}
