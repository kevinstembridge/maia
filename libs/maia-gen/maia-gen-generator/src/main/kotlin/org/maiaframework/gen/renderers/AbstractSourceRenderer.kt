package org.maiaframework.gen.renderers


abstract class AbstractSourceRenderer {


    protected val sourceCode = StringBuilder()


    protected abstract fun renderSource(): String


    protected fun renderGeneratedCodeStatement() {

        appendLine("// This source was generated by the Mahana code generator")
        appendLine("// Renderer class: $javaClass")

    }


    protected fun getSourceCode(): String {

        return this.sourceCode.toString()

    }


    fun append(text: String) {

        this.sourceCode.append(text)

    }


    protected fun append(text: String, vararg args: Any) {

        this.sourceCode.append(String.format(text, *args))

    }


    fun appendLine(text: String) {

        append(text)
        newLine()

    }


    protected fun appendLine(text: String, vararg args: Any) {

        append(String.format(text, *args))
        newLine()

    }


    fun blankLine() {

        newLine()

    }


    fun newLine() {

        append("\n")

    }


}
